package com.example.chapter13.bean;

public class UserInfo {
	public String name; // 姓名
	public int age; // 年龄
	public long height; // 身高
	public float weight; // 体重
	
	public UserInfo(String name, int age, long height, float weight) {
		this.name = name;
		this.age = age;
		this.height = height;
		this.weight = weight;
	}
}
